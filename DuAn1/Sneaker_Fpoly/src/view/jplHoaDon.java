    public void fillTable() {
        ArrayList<HoaDon> _lstHoaDon = hoaDonRepository.getAllHoaDon(soTrang);
        tblModel.setRowCount(0);
        String column[] = {"Mã Hóa Đơn", "Nhân Viên", "Khách Hàng", "Thành Tiền", "Khuyến Mãi", "Ghi Chú", "Ngày Tạo",  "Trạng Thái"};
        tblModel.setColumnIdentifiers(column);
        for (HoaDon hoaDon : _lstHoaDon) {
            tblModel.addRow(new Object[]{
                hoaDon.getMaHoaDon(), hoaDon.getIdNhanVien().getHoVaTen(), hoaDon.getIdKhachHang().getTenKhachHang()==null?"khach le*":hoaDon.getIdKhachHang().getTenKhachHang(), hoaDon.getThanhTien(), hoaDon.getIdKhuyenMai().getTenKhuyenMai(),  hoaDon.getGhiChu(), hoaDon.getNgayTao(), hoaDon.getTrangThai()
            });
        }
        lblHoaDon.setText(soTrang + "/" + SoBanGhiHoaDon());
        tblHoaDon.setModel(tblModel);
    }

    public int SoBanGhiHoaDon() {
        int soBanGhi;
        soBanGhi = hoaDonRepository.SoBanGhi();
        int tongTrang = soBanGhi / page;
        return tongTrang;
    }

    public int SoBanGhiHoaDonChiTiet() {
        int soBanGhi = hoaDonChiTietRepository.SoBanGhiHoaDonChiTiet();
        int tongTrang = soBanGhi / page;
        return tongTrang;
    }

