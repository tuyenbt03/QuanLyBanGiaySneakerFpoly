    private void btnLocActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLocActionPerformed
        // TODO add your handling code here:
        Date ngayBatDau = txtTuNgay.getDate();
        Date ngayKetThuc = txtDenNgay.getDate();

        if (ngayBatDau != null && ngayKetThuc != null) {
            long khoangCach = ngayKetThuc.getTime() - ngayBatDau.getTime();
            long soNgay = khoangCach / (24 * 60 * 60 * 1000); // Chuyển đổi sang số ngày

            if (soNgay > 30) {
                JOptionPane.showMessageDialog(this, "Khoảng cách giữa ngày bắt đầu và ngày kết thúc không được lớn hơn 30 ngày", "Thông báo", JOptionPane.WARNING_MESSAGE);
                return;
            }
        } else {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn cả ngày bắt đầu và ngày kết thúc", "Thông báo", JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (ngayBatDau == null) {
            JOptionPane.showMessageDialog(this, "Ngày bắt đầu không được để trống");
            return;
        }
        if (ngayKetThuc == null) {
            JOptionPane.showMessageDialog(this, "Ngày kết thúc không được để trống");
            return;
        }
        if (ngayBatDau.getTime() > ngayKetThuc.getTime()) {
            JOptionPane.showMessageDialog(this, "Ngày bắt đầu phải nhỏ hơn ngày kết thúc");
            return;
        }

        String kieuNgay = (String) cboNgayTao.getSelectedItem();
        if (kieuNgay.equals("Ngày tạo")) {
            kieuNgay = "NgayTao";
        } else if (kieuNgay.equals("Ngày thanh toán")) {
            kieuNgay = "NgayMua";
        }
        ArrayList<HoaDon> newHoaDon = hoaDonRepository.getHoaDonByDate(ngayKetThuc, ngayBatDau, kieuNgay);
        tblModel.setRowCount(0);
        String column[] = {"Mã Hóa Đơn", "Nhân Viên", "Khách Hàng", "Thành Tiền", "Khuyến Mãi", "Ghi Chú", "Ngày Mua", "Ngày Tạo", "Trạng Thái"};
        tblModel.setColumnIdentifiers(column);
        for (HoaDon hoaDon : newHoaDon) {
            tblModel.addRow(new Object[]{
                hoaDon.getMaHoaDon(), hoaDon.getIdNhanVien().getHoVaTen(), hoaDon.getIdKhachHang().getTenKhachHang(), hoaDon.getThanhTien(), hoaDon.getIdKhuyenMai().getTenKhuyenMai(), hoaDon.getGhiChu(), hoaDon.getNgayMua(), hoaDon.getNgayTao(),hoaDon.getTrangThai()
            });
        }
        tblHoaDon.setModel(tblModel);
    }//GEN-LAST:event_btnLocActionPerformed

