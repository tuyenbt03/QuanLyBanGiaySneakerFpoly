    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        if (checkTrongSP()) {
            if (checkMaSP()) {
                if (checkTenSP()) {
                    UUID id = UUID.randomUUID();
                    String maSP = this.txtMaCL.getText();
                    String tenSP = this.txtTenCL.getText();
                    int trangThai = 1;
                    if (rdoHetCL.isSelected()) {
                        trangThai = 0;
                    }

                    ChatLieu sp = new ChatLieu(String.valueOf(id), maSP, tenSP, trangThai);

                    boolean a = this.service.insert(sp);

                    if (a == true) {
                        JOptionPane.showMessageDialog(this, "Thêm thành công");
                    } else {
                        JOptionPane.showMessageDialog(this, "Thêm thất bại");
                        return;
                    }
                    this.clear();
                    this.loadTableSCL();
                    chatlieu.loadCbb();
                }
            }
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaSPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaSPActionPerformed
        int index = tblChatLieuConHoatDong.getSelectedRow();
        if (index >= 0) {
            listSP = service.getAllChatLieuConHoatDong();
            int trangThai = 1;
            if (rdoHetCL.isSelected()) {
                trangThai = 0;
            }
            String ma = txtMaCL.getText();
            String ten = txtTenCL.getText();
            ChatLieu sp = new ChatLieu();
            sp.setMaChatlieu(ma);
            sp.setTenChatLieu(ten);
            sp.setTrangThai(trangThai);
            if (ma.equalsIgnoreCase(sp.getMaChatlieu()) && ten.equalsIgnoreCase(sp.getTenChatLieu())) {
                if (checkTrongSP()) {
                    service.update(sp);
                    this.loadTableSCL();
                    JOptionPane.showMessageDialog(this, "Sửa thành công");
                      chatlieu.loadCbb();
                    clear();
                } else {
                    JOptionPane.showMessageDialog(this, "Không được để trống");

                }
            } else if (!ma.equalsIgnoreCase(sp.getMaChatlieu())) {
                if (checkTrongSP()) {
                    if (checkMaSP()) {
                        service.update(sp);
                        this.loadTableSCL();
                        JOptionPane.showMessageDialog(this, "Sửa thành công");
                          chatlieu.loadCbb();
                        clear();
                    } else {
                        JOptionPane.showMessageDialog(this, "Mã sản phẩm bị trùng");
                    }
                }
            } else if (!ten.equalsIgnoreCase(sp.getTenChatLieu())) {
                if (checkTrongSP()) {
                    if (checkTenSP()) {
                        service.update(sp);
                        this.loadTableSCL();
                        JOptionPane.showMessageDialog(this, "Sửa thành công");
                          chatlieu.loadCbb();
                        clear();
                    } else {
                        JOptionPane.showMessageDialog(this, "Tên sản phẩm bị trùng");

                    }
                }
            } else {
                if (checkTrongSP()) {
                    if (checkMaSP()) {
                        if (checkTenSP()) {
                            service.update(sp);
                            this.loadTableSCL();

                        }
                    }
                }
            }
        } else
            JOptionPane.showMessageDialog(this, "Vui lòng chọn dòng cần sửa!");
    }//GEN-LAST:event_btnSuaSPActionPerformed

