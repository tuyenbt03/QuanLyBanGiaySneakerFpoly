    private void loadTableSCL() {
        ArrayList<ChatLieu> list = (ArrayList<ChatLieu>) this.service.getAllChatLieuConHoatDong();
        dtm.setRowCount(0);

        for (ChatLieu sp : list) {
            dtm.addRow(new Object[]{
                sp.getMaChatlieu(),
                sp.getTenChatLieu(),
                sp.getNgayTao(),
                sp.getNgaySua(),
                sp.getTrangThai() == 1 ? "Còn hàng" : "Hết hàng"
            });
        }
    }

