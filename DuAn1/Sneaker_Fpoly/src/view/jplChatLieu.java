    private void tblDoiTrangThaiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tblDoiTrangThaiActionPerformed
        // TODO add your handling code here:
        List<ChatLieu> lstChatLieuConHoatDong = service.getAllChatLieuConHoatDong();
        int selectedtRow1 = tblChatLieuConHoatDong.getSelectedRow();
        List<ChatLieu> lstChatLieuNgungHoatDong = service.getAllChatLieuNgungHoatDong();
        int selectedRow2 = tblChatLieuNgungHoatDong.getSelectedRow();

        if (selectedtRow1 >= 0) {
            if (selectedtRow1 >= 0 && selectedRow2 < lstChatLieuConHoatDong.size()) {
                String idChoice = lstChatLieuConHoatDong.get(selectedtRow1).getId();
                // Sử dụng idChoice ở đây
                service.updateTrangThaiChatLieu(idChoice);
            } else {
                JOptionPane.showMessageDialog(this, "Bạn phải chọn dòng");
            }
        } else if (selectedRow2 >= 0) {
            if (selectedRow2 >= 0 && selectedRow2 < lstChatLieuNgungHoatDong.size()) {
                String idChoice = lstChatLieuNgungHoatDong.get(selectedRow2).getId();
                // Sử dụng idChoice ở đây
                service.updateTrangThaiChatLieu(idChoice);
            } else {
                JOptionPane.showMessageDialog(this, "Bạn phải chọn dòng");
            }
        }
        clear();
        loadTableSCL2();
        loadTableSCL();
    }//GEN-LAST:event_tblDoiTrangThaiActionPerformed

    public void showDataTablePT(List<ChatLieu> lists) {
        dtm.setRowCount(0);
        dtm2.setRowCount(0);
        List<ChatLieu> lstChatLieuConHang = new ArrayList<>();
        List<ChatLieu> lstChatLieuHetHang = new ArrayList<>();
        for (ChatLieu chatLieu : lists) {
            if(chatLieu.getTrangThai()==1){
                lstChatLieuConHang.add(chatLieu);
            }else{
                lstChatLieuHetHang.add(chatLieu);
            }
        }
        for (ChatLieu chatLieu : lstChatLieuConHang) {
            dtm.addRow(chatLieu.toDataRow());
        }
        for (ChatLieu chatLieu : lstChatLieuHetHang) {
            dtm2.addRow(chatLieu.toDataRow());
        }
    }

    public void clickVaoBang(int index) {
        listSP = service.getAllChatLieuConHoatDong();
        ChatLieu sp = listSP.get(index);

        txtMaCL.setText(sp.getMaChatlieu());
        txtTenCL.setText(sp.getTenChatLieu());
        if (sp.getTrangThai() == 1) {
            rdoConCL.setSelected(true);
        } else {
            rdoHetCL.setSelected(true);
        }
    }

    public boolean checkTrongSP() {
        if (txtMaCL.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Mã không được để trống!");
            return false;
        } else if (txtTenCL.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Tên không được để trống!");
            return false;
        } else {
            return true;
        }
    }

    public boolean checkMaSP() {
        String ma = txtMaCL.getText().trim();
        for (String s : listMaSP) {
            if (ma.equalsIgnoreCase(s)) {
                JOptionPane.showMessageDialog(this, "Mã đã tồn tại!");
                return false;
            }
        }
        return true;
    }

    public boolean checkTenSP() {
        String ten = txtMaCL.getText().trim();
        for (String s : listTenSP) {
            if (ten.equalsIgnoreCase(s)) {
                JOptionPane.showMessageDialog(this, "Tên đã tồn tại!");
                return false;
            }
        }
        return true;
    }

    private void clear() {

        this.txtMaCL.setText("");
        this.txtTenCL.setText("");
    }

